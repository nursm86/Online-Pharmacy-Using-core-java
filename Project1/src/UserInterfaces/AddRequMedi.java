    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String name = jTextField1.getText().trim();
        String power = jTextField3.getText().trim();
        String company = jTextField2.getText();
        String quantity = jTextField4.getText();
        String price = jTextField5.getText();

        if(verifyFields()){

            PreparedStatement hehe;
            ResultSet he;
            String mquery = "SELECT * FROM `medicine` WHERE `mname` = ? AND `power` = ?";

            try {
                hehe = My_CNX.getConnection().prepareStatement(mquery);
                hehe.setString(1, name);
                hehe.setString(2,power);

                he = hehe.executeQuery();

                if(he.next()){

                    PreparedStatement hehe2;
                    ResultSet he2;
                    String mquery2 = "UPDATE `medicine` SET `quantity` = `quantity`+? WHERE `mname` = ? AND `power` = ?";

                    hehe2 = My_CNX.getConnection().prepareStatement(mquery2);
                    hehe2.setString(1,quantity);
                    hehe2.setString(2, name);
                    hehe2.setString(3,power);

                    hehe2.executeUpdate();
                    
                    JOptionPane.showMessageDialog(null, "Medicine Added");

                }
                else{
                    PreparedStatement st;
                    String query = "INSERT INTO `medicine`(`mname`, `power`, `company`, `quantity`, `price(per tab)`) VALUES (?,?,?,?,?)";

                    try {
                        st = My_CNX.getConnection().prepareStatement(query);
                        st.setString(1,name);
                        st.setString(3,power);
                        st.setString(2,company);
                        st.setString(4,quantity);
                        st.setString(5,price);

                        st.executeUpdate();

                        JOptionPane.showMessageDialog(null, "Medicine Added");
                    } catch (SQLException ex) {
                        Logger.getLogger(Register_Form.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            } catch (SQLException ex) {
                Logger.getLogger(AddMedicine.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
        PreparedStatement st2;
        String queryy = "DELETE FROM `requ_medi` WHERE `mname` = ? AND `power` = ?";
        
        try {
            st2 = My_CNX.getConnection().prepareStatement(queryy);
            st2.setString(1,name);
            st2.setString(2,power);
            
            st2.executeUpdate();
            
        } catch (SQLException ex) {
            Logger.getLogger(AddRequMedi.class.getName()).log(Level.SEVERE, null, ex);
        }
        Requested_Medi.fillTable();
        dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

